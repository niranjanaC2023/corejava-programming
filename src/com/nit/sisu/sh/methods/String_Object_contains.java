package com.nit.sisu.sh.methods;
class String_Object_contains 	
{
	public static void main(String[] args) 
	{
		String s1 = "JavaHariKrishna";
		//System.out.println(s1.contains('a'));
		System.out.println();

		System.out.println(s1.contains("a"));
		System.out.println();

		System.out.println(s1.contains("A"));
		System.out.println();

		System.out.println(s1.contains("Hari"));
		System.out.println();

		System.out.println(s1.contains("hari"));
		System.out.println();

		System.out.println(s1.equals("Hari"));
		System.out.println();

		System.out.println(s1.contains("Hari"));
		System.out.println();

		System.out.println(s1.equals("JavaHari"));
		System.out.println();

		System.out.println(s1.contains("JavaHari"));
		System.out.println();

		System.out.println(s1.contains(new StringBuffer("ava")));
		System.out.println();
	}
}

		
	

		