package com.nit.sisu.collection;
public class valueOf_Method
{
	public static void main(String[] args)
	{
	int i1 = 1992;
	String s1 = String.valueOf(i1);
	System.out.println(s1+" Niranjana's Birthday");
//*********************************************************
	float f1 = 1992f;
	String s2 = String.valueOf(f1);
	System.out.println(s2+" Niranjana's Birthday");
	}
	
	//we can use String.valueOf(),Integer.toString(),String.format() to convert int to String.
	//we can use Integer.parseInt(),Integer.valueOf() to convert String to int.
}