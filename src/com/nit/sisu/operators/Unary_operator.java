package com.nit.sisu.operators;
class Unary_operator 
{
	public static void main(String[] args) 
	{
		int i1 = +5;
		System.out.println(i1);

		int i2 = -6;
		System.out.println(i2);

		boolean b1 = true;
		System.out.println(b1);

		System.out.println(~5);			//(output: -6 )

		System.out.println(~10);		////(output: -11 )

		System.out.println(~-50);		//(output: 49 )

		System.out.println(~-28);		//(output: 27 )

	




	}
}
